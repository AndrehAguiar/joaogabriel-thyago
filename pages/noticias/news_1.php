    <p> 
        Uma nova dupla acaba de surgir no cen&aacute;rio musical brasileiro: Jo&atilde;o Gabriel & Thyago. Seu primeiro disco “Uma Can&ccedil;&atilde;o Pra Voc&ecirc;” ser&aacute; lan&ccedil;ado no in&iacute;cio de 2017, com 14 faixas autorais. A m&uacute;sica de estreia &eacute; “Uma Can&ccedil;&atilde;o Pra Voc&ecirc;” e vai conquistar os mais exigentes ouvintes da boa m&uacute;sica sertaneja.

        </p>
		<p> 

 
			A dupla possui um repert&oacute;rio variado, que perpassa todas as fases da m&uacute;sica sertaneja; desde a tradicional moda rancheira at&eacute; as modas atuais. “Uma Can&ccedil;&atilde;o Pra Voc&ecirc;” traz uma variedade de m&uacute;sicas para atender um p&uacute;blico diverso, proporcionando aos f&atilde;s se identificar e se emocionar com suas can&ccedil;&otilde;es.

        </p>
		<p> 


 
			O que os diferencia das demais duplas &eacute; seu estilo pr&oacute;prio de interpreta&ccedil;&atilde;o, com vozes fortes, presentes e inconfund&iacute;veis que ir&atilde;o emocionar de verdade, al&eacute;m de uma &oacute;tima desenvoltura, habilidade instrumental, boa intera&ccedil;&atilde;o com o p&uacute;blico e a simplicidade natural.

        </p>
		<p> 


 
			 Jo&atilde;o Gabriel e Thyago cresceram na cidade de Lagoa Santa (MG), onde se conheceram e resolveram se unir pelo amor à m&uacute;sica. Hoje, s&atilde;o amigos-irm&atilde;os e pretendem ganhar o cora&ccedil;&atilde;o do pa&iacute;s com seu CD de lan&ccedil;amento e mostrar ao p&uacute;blico o seu estilo pr&oacute;prio e harmonia contagiante. Suas vozes marcantes, al&eacute;m do carisma e o desejo de resgatar a m&uacute;sica sertaneja tradicional, com a valoriza&ccedil;&atilde;o das composi&ccedil;&otilde;es autorais &eacute; o que destaca sua qualidade profissional.

        </p>
		<p> 


 
			Gabriel da Fonseca Salom&atilde;o, o Jo&atilde;o Gabriel, &eacute; natural de Belo Horizonte, tem 36 anos e &eacute; o compositor da dupla. Ele toca viola caipira e viol&atilde;o e, al&eacute;m da m&uacute;sica, adora fazendas e cria&ccedil;&atilde;o de gado e cavalos e um de seus sonhos &eacute; ter o seu peda&ccedil;o de terra no futuro. Seus &iacute;dolos s&atilde;o Jo&atilde;o Paulo e Daniel, que o inspiraram a ouvir a m&uacute;sica sertaneja e a aprender a cantar. “Ser m&uacute;sico sempre foi o meu sonho e na minha adolesc&ecirc;ncia comecei a tomar gosto pelo estilo sertanejo, que se tornou o meu preferido. O que mais desejo &eacute; ter uma carreira s&oacute;lida, conquistar o reconhecimento das pessoas e as ouvir cantando nossas m&uacute;sicas. É poder bater no peito e dizer que sou um vencedor!”.

        </p>
		<p> 


 
			J&aacute; Thyago, ou melhor - Tiago Pereira dos Santos - tem 29 anos, nasceu em Vespasiano (MG) e toca viol&atilde;o e contrabaixo. Formado em Hist&oacute;ria, com p&oacute;s-gradua&ccedil;&atilde;o em Hist&oacute;ria e Cultura Mineira, sua verdadeira paix&atilde;o &eacute; a m&uacute;sica que, para ele, &eacute; uma oportunidade de transcender. O estilo sertanejo &eacute; um dos seus favoritos. Suas principais refer&ecirc;ncias musicais s&atilde;o as duplas Gian e Giovanni, Milion&aacute;rio e Jos&eacute; Rico e Ti&atilde;o Carreiro e Pardinho. “Nosso maior sonho &eacute; fazer sucesso e poder levar a nossa m&uacute;sica para todos os cantos do Brasil. Vamos trabalhar com seriedade para nos igualarmos aos grandes expoentes da m&uacute;sica sertaneja, realizar grandes shows, nos apresentar em grandes eventos e, principalmente, chegar um dia a ser atra&ccedil;&atilde;o principal em Barretos. O p&uacute;blico que queremos conquistar s&atilde;o os amantes da boa m&uacute;sica sertaneja de todas as idades”.
